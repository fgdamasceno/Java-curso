
public class Program {
	
	public static void main(String[] args) {
		
		// do while
		// primeiro executo o bloco 'do'
		// depois a condição é avaliada em 'while'
		// se 'V', repete o 'do', se 'F' sai do bloco
		
		int count = 0;
		
		do {
			System.out.println(count);
			count++;
		} while (count < 10);
		
	}

}
